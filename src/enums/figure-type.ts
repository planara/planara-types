/**
 * Типы базовых фигур.
 * Используется для обозначения геометрии объекта.
 */
export enum FigureType {
  /** Куб */
  Cube,

  /** Сфера */
  Sphere,

  /** Плоскость */
  Plane,

  /** Цилиндр */
  Cylinder,

  /** Конус */
  Cone,

  /** Тор */
  Torus,

  /** Круг */
  Circle,

  /** Икосфера */
  Icosphere,

  /** UV-сфера */
  UVSphere,

  /** Кастомная фигура */
  Custom,
}
